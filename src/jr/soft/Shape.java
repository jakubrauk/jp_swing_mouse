package jr.soft;

import java.awt.Graphics2D;

public abstract class Shape {

    protected int x, y;

    public Shape(int x, int y) {
        super();
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public abstract void draw(Graphics2D g2d);
}
